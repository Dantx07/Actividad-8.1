package Personas;

public class Persona {
        String Nombre;
        String Apellido;
        String Telefono;
        String Direccion;

    public Persona(String Nombre, String Apellido, String Telefono, String Direccion) {
        this.Nombre = Nombre;
        this.Apellido = Apellido;
        this.Telefono = Telefono;
        this.Direccion = Direccion;
    }
}
